                                At Ahava Development Initiative, we believe the well-being of individuals determines the value of an outcome, and the health of our community begins with better access to food. Hence, we hope to reduce these barriers by providing food pantries and inspiring others to give. We are encouraging and establishing food pantries in faith-based organisations and other unifying institutions.  The food pantry is a temporary food assistance program designed to collect and distribute food and household essentials for free to low-income and at-risk members of the community. Hence, it is only accessible twice a year, in August & December on specified dates. The materials provided should sustain a family of 5 for about 2-4 weeks. The initiative has been introduced in Benue to encourage other members of the community to bring fresh produce from their various farms during harvest time to create a balanced nutritional value to other people who do not produce similar products, and to help other members of the community who are experiencing short-term hardships such as widows and those who experienced temporary farm losses. Our organization’s overarching goal is to redesign a social system that prioritises relational well-being, brotherliness, productivity, moral order and communion rather than display, competition, power, or greed. <br><br>

